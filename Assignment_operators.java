public class Assignment_operators {
    public static void main(String[] args) {

        int a=4;
        int b=a;
        System.out.println("value of a :"+a);//4
        System.out.println("value of b :"+b);//4

        b+=3;
        System.out.println("value of b = "+b);//7
        System.out.println(b-=1);//6
        System.out.println(b*=1);//6
        System.out.println(b/=2);//3
        System.out.println(b%=2);//1
    
      
    }
    
}

//Assignment operator in java,denoted by (=) use to assign a value to variable 